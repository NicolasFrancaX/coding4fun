#include <bits/stdc++.h>

using namespace std;

int main() {
    cout << "Valores de b:" << endl;

    cout << "-------------" << endl;

    cout << "1) b = 7" << endl;

    cout << "2) b = " << setw(20) << "7" << endl;

    cout << "3) b = 00000000000000000007" << endl;

    cout << "4) b = 7" << endl;

    cout << "5) b = 7%" << endl << endl;

    cout << "Valores de d:" << endl;

    cout << "-------------" << endl;

    cout << "1) d = 2.208000" << endl;

    cout << "2) d = 2" << endl;

    cout << "3) d = 2.2" << endl;

    cout << "4) d = 2.21" << endl;

    cout << "5) d = 2.208" << endl;

    cout << "6) d = " << setw(20) << "2.208" << endl;

    cout << "7) d = 0000000000000002.208" << endl;

    cout << "8) d = 2.208" << endl;

    cout << "9) d = 2.21%" << endl << endl;
    return 0;
}
